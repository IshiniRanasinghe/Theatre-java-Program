public class Person {
    String name;
    String surname;
    String email;

    public Person(String name, String surname, String email) {
        this.name = name;
        this.surname = surname;
        this.email = email;
    }
    public Person(){
        this.name = "Name";
        this.surname = "surname";
        this.email = "email";

    }
}
